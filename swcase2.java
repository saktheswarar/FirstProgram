package sakthe2;

public class swcase2 {
      public static void main (String args[] ) {
    	  char a='^';
    	   
    	  switch(a) {
    	  
    	  case 23:
  			System.out.println("case 1");
  			break;
  			
  		case 56:
  			System.out.println("case 2");
  			break;
  			
  		default:
  			System.out.println("a");
  			break;
    	  }
      }
}
